def compare_seasonality(reference, target, ref_label, target_label="2025â€“2026"):
    # Convert to arrays
    y_true = reference.values
    y_pred = target.values

    # Correlation
    corr = np.corrcoef(y_true, y_pred)[0, 1]

    # MSE and Normalized MSE
    mse = mean_squared_error(y_true, y_pred)
    nmse = mse / np.var(y_true)

    print(f"ðŸ“Š Comparing {target_label} to {ref_label}:")
    print(f"   ðŸ” Correlation       = {corr:.3f}")
    print(f"   ðŸ“‰ MSE               = {mse:.3f}")
    print(f"   âš–ï¸ Normalized MSE    = {nmse:.3f}")
    print("-" * 50)

